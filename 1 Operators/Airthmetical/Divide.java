class Divide {
    public static void main(String[] args) {
        int x, y;
        x = 3;
        y = 96;
        System.out.println("x /y :" + (float)(x  / y));
        System.out.println("y /x :" + y  / x);
        System.out.println("-x/y :" + -x /y);
        System.out.println("-y/x :" + -y /x);
        System.out.println("x /-y :" + x / -y);
        System.out.println("y /-x :" + y / -x);
        System.out.println("-x/-y :"+ -x /-y);
        System.out.println("-y/-x :" + y /x);
    }
}
