
import java.util.Scanner;

public class FL7 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("enter the value of nth term : ");
        int n = sc.nextInt();
        int a;
        for (a = 1; a <= n; a++) {
            System.out.print(a * a + " ");

        } 
    }
}
